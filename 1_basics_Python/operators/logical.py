# Logical Operators
# Logical operators are the and, or, not operators.

X=True
Y=False
Z=True
# The and operator returns True if both the operands are True.
print("True and True is",X and Z) # Output: True
print(f"{X} and {Y} is {X and Y}")


# The or operator returns True if one of the operands is True.
print(f"{X} or {Y} is {X or Y}")

# The not operator returns True if the operand is False.
print(f"not {X} is {not X}")
print(f"not {Y} is {not Y}")

